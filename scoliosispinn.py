def siitrc_model(t, y, a, l, p, r, q, t_L, t_H):

    def func(y, t):
        S, I_L, I_H, T, R, C = y
        dS = -a * S
        dI_L = q * a * S + r * R - t_L * I_L
        dI_H = (1-q) * a * S - t_H * I_H
        dT = t_L * I_L + t_H * I_H - l * T
        dR = p * l * T - r * R
        dC = (1-p) * l * T
        return [dS, dI_L, dI_H, dT, dR, dC]

    S_0 = N
    I_H_0 = 0
    I_L_0 = 0
    T_0 = 0
    R_0 = 0
    C_0 = 0
    y0 = [S_0, I_L_0, I_H_0, T_0, R_0, C_0]
    return odeint(func, y0, t)

