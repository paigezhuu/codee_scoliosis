def dinn(data_t, data_y, N, noise_level, variable_filename="variables.dat"):
    init_val = -2.197
    a_raw = dde.Variable(init_val)
    l_raw = dde.Variable(init_val)
    p_raw = dde.Variable(init_val)
    r_raw = dde.Variable(init_val)
    q_raw = dde.Variable(init_val)
    t_L_raw = dde.Variable(init_val)
    t_H_raw = dde.Variable(init_val)

    variable_list = [a_raw, l_raw, p_raw, r_raw, q_raw, t_L_raw, t_H_raw]

    def ODE(t, y_pred):
        a = tf.math.sigmoid(a_raw)
        l = tf.math.sigmoid(l_raw)
        p = tf.math.sigmoid(p_raw)
        r = tf.math.sigmoid(r_raw)
        q = tf.math.sigmoid(q_raw)
        t_L = tf.math.sigmoid(t_L_raw)
        t_H = tf.math.sigmoid(t_H_raw)

        S_pred = y_pred[:, 0:1]
        I_L_pred = y_pred[:, 1:2]
        I_H_pred = y_pred[:, 2:3]
        T_pred = y_pred[:, 3:4]
        R_pred = y_pred[:, 4:5]
        C_pred = y_pred[:, 5:6]

        dS_pred_t = dde.grad.jacobian(y_pred, t, i=0)
        dI_L_pred_t = dde.grad.jacobian(y_pred, t, i=1)
        dI_H_pred_t = dde.grad.jacobian(y_pred, t, i=2)
        dT_pred_t = dde.grad.jacobian(y_pred, t, i=3)
        dR_pred_t = dde.grad.jacobian(y_pred, t, i=4)
        dC_pred_t = dde.grad.jacobian(y_pred, t, i=5)

        return [
            dS_pred_t - (-a * S_pred),
            dI_L_pred_t - (q * a * S_pred + r * R_pred - t_L * I_L_pred),
            dI_H_pred_t - ((1 - q) * a * S_pred - t_H * I_H_pred),
            dT_pred_t - (t_L * I_L_pred + t_H * I_H_pred - l * T_pred),
            dR_pred_t - (p * l * T_pred - r * R_pred),
            dC_pred_t - ((1 - p) * l * T_pred),
        ]

    geom = dde.geometry.TimeDomain(data_t[0, 0], data_t[-1, 0])

    y_noise = data_y + np.random.normal(loc=0, scale=data_y.std(axis=0), size=data_y.shape) * noise_level

    # Train data
    observe_S = dde.icbc.PointSetBC(data_t, y_noise[:, 0:1], component=0)
    observe_I_L = dde.icbc.PointSetBC(data_t, y_noise[:, 1:2], component=1)
    observe_I_H = dde.icbc.PointSetBC(data_t, y_noise[:, 2:3], component=2)
    observe_T = dde.icbc.PointSetBC(data_t, y_noise[:, 3:4], component=3)
    observe_R = dde.icbc.PointSetBC(data_t, y_noise[:, 4:5], component=4)
    observe_C = dde.icbc.PointSetBC(data_t, y_noise[:, 5:6], component=5)


    # Model
    data = dde.data.PDE(
        geom,
        ODE,
        [
            observe_S,
            observe_I_L,
            observe_I_H,
            observe_T,
            observe_R,
            observe_C
        ],
        num_domain=0,
        num_boundary=0,
        anchors=data_t,
    )

    net = dde.nn.FNN([1] + [64] * 3 + [6], "swish", "Glorot normal")

    def feature_transform(t):
        t = t / data_t[-1, 0]
        return t

    net.apply_feature_transform(feature_transform)

    iterations = 50000
    model = dde.Model(data, net)
    model.compile(
        "adam",
        lr=1e-3,
        loss_weights=6 * [1] + 6 * [1],
        external_trainable_variables=variable_list
    )
    variable = dde.callbacks.VariableValue(
        variable_list,
        period=5000,
        filename=variable_filename
    )
    losshistory, train_state = model.train(
        iterations=iterations,
        display_every=10000,
        callbacks=[variable]
      )
    dde.saveplot(losshistory, train_state, issave=True, isplot=True)
    return model, variable

