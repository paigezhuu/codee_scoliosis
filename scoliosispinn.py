"""# Model

$$
\begin{aligned}
\frac{dS}{dt} &= - \alpha  S \\
\frac{dI^L}{dt} &= q \alpha S + \rho R - \tau^L I^H \\
\frac{dI^H}{dt} &= (1-q) \alpha S - \tau^H I^H \\
\frac{dT}{dt} &= \tau^L I^L + \tau^H I^H - \lambda T \\
\frac{dR}{dt} &= p \lambda T - \rho R\\
\frac{dC}{dt} &= (1-p) \lambda T \\
\end{aligned}
$$
"""

